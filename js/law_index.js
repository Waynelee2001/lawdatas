/**
 * 中文数字转阿拉伯数字
 */
function chineseNumToArabic(str) {
  const numMap = {
    '零': 0, '〇': 0, '○': 0,
    '一': 1, '二': 2, '三': 3, '四': 4, '五': 5,
    '六': 6, '七': 7, '八': 8, '九': 9, '十': 10,
    '百': 100, '千': 1000
  };

  let result = 0;
  let temp = 0;

  for (let i = 0; i < str.length; i++) {
    const char = str[i];
    const num = numMap[char];

    if (num === undefined) continue;

    if (num >= 10) {
      if (temp === 0) temp = 1;
      if (num === 100 || num === 1000) {
        result += temp * num;
        temp = 0;
      } else {
        // 十
        if (i === 0) {
          result += 10;
          temp = 0;
        } else {
          result += temp * 10;
          temp = 0;
        }
      }
    } else {
      temp = num;
    }
  }
  result += temp;

  return result;
}

/**
 * 规范化法条号（中文转阿拉伯数字）
 */
function normalizeArticleNum(articleStr) {
  const match = articleStr.match(/第([一二三四五六七八九十百零〇○]+)条(之([一二三四五六七八九十]+))?/);
  if (!match) return articleStr;

  const mainNum = chineseNumToArabic(match[1]);
  const subNum = match[3] ? chineseNumToArabic(match[3]) : null;

  if (subNum) {
    return `第${mainNum}条之${subNum}`;
  }
  return `第${mainNum}条`;
}

